package com.atozmediaapk.textilefocus;


public class NotificationData {
    public NotificationData(String mSurname1, String mEmail1) {
        this.mSurname1 = mSurname1;
        this.mEmail1 = mEmail1;
        this.mImage1 = mImage1;
    }

    private String mSurname1;

    public String getmSurname1() {
        return mSurname1;
    }

    public void setmSurname1(String mSurname1) {
        this.mSurname1 = mSurname1;
    }

    public String getmEmail1() {
        return mEmail1;
    }

    public void setmEmail1(String mEmail1) {
        this.mEmail1 = mEmail1;
    }

    public String getmImage1() {
        return mImage1;
    }

    public void setmImage1(String mImage1) {
        this.mImage1 = mImage1;
    }

    private String mEmail1;
    private String mImage1;


}
